import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const schemasDir = path.join(repoRoot, 'schemas');
const outDir = path.join(repoRoot, 'generated', 'typescript');
const outFile = path.join(outDir, 'index.d.ts');

async function loadCompiler() {
  try {
    return await import('json-schema-to-typescript');
  } catch (error) {
    if (error && error.code === 'ERR_MODULE_NOT_FOUND') {
      throw new Error('Missing Node dependencies. Run `npm install` before generating TypeScript.');
    }
    throw error;
  }
}

async function walkJsonSchemas(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const fullPath = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...await walkJsonSchemas(fullPath));
    } else if (entry.isFile() && entry.name.endsWith('.schema.json')) {
      files.push(fullPath);
    }
  }
  return files.sort();
}

function toTypeName(schema) {
  return String(schema.title || 'Anonymous')
    .replace(/[^A-Za-z0-9]+/g, ' ')
    .trim()
    .split(/\s+/)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join('');
}

function pointerGet(value, pointer) {
  if (!pointer || pointer === '#') {
    return value;
  }
  if (!pointer.startsWith('#/')) {
    throw new Error(`Unsupported JSON pointer fragment: ${pointer}`);
  }
  return pointer.slice(2).split('/').reduce((current, token) => {
    const key = token.replace(/~1/g, '/').replace(/~0/g, '~');
    if (current == null || !(key in current)) {
      throw new Error(`Unresolvable JSON pointer fragment: ${pointer}`);
    }
    return current[key];
  }, value);
}

function sanitizeDefinitionKey(uri) {
  return uri
    .replace(/^https:\/\/schemas\.ubunow\.net\/phase1\//, '')
    .replace(/\.schema\.json/g, '')
    .replace(/[^A-Za-z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '');
}

function bundleSchema(rootSchema, registry) {
  const root = structuredClone(rootSchema);
  const definitions = root.$defs ? structuredClone(root.$defs) : {};
  const seen = new Set();

  function rewrite(node) {
    if (Array.isArray(node)) {
      node.forEach(rewrite);
      return;
    }
    if (!node || typeof node !== 'object') {
      return;
    }

    if (typeof node.$ref === 'string' && node.$ref.startsWith('https://schemas.ubunow.net/phase1/')) {
      const [base, fragment = ''] = node.$ref.split('#');
      const targetSchema = registry.get(base);
      if (!targetSchema) {
        throw new Error(`Unregistered schema $id in $ref: ${node.$ref}`);
      }
      const key = sanitizeDefinitionKey(`${base}${fragment ? `_${fragment}` : ''}`);
      node.$ref = `#/$defs/${key}`;
      if (!seen.has(key)) {
        seen.add(key);
        definitions[key] = structuredClone(pointerGet(targetSchema, fragment ? `#${fragment}` : '#'));
        rewrite(definitions[key]);
      }
    }

    for (const value of Object.values(node)) {
      rewrite(value);
    }
  }

  rewrite(root);
  if (Object.keys(definitions).length > 0) {
    root.$defs = definitions;
  }
  return root;
}

async function main() {
  const { compile } = await loadCompiler();
  const files = await walkJsonSchemas(schemasDir);
  const registry = new Map();

  for (const file of files) {
    const schema = JSON.parse(await fs.readFile(file, 'utf8'));
    if (!schema.$id) {
      throw new Error(`Schema missing $id: ${file}`);
    }
    if (registry.has(schema.$id)) {
      throw new Error(`Duplicate schema $id: ${schema.$id}`);
    }
    registry.set(schema.$id, schema);
  }

  await fs.mkdir(outDir, { recursive: true });

  const chunks = [
    '/* Generated by scripts/generate-typescript.sh. Do not edit by hand. */',
    ''
  ];

  for (const schema of registry.values()) {
    const bundled = bundleSchema(schema, registry);
    const ts = await compile(bundled, toTypeName(schema), {
      bannerComment: '',
      cwd: repoRoot,
      declareExternallyReferenced: false,
      unreachableDefinitions: true
    });
    chunks.push(ts.trim(), '');
  }

  await fs.writeFile(outFile, chunks.join('\n'), 'utf8');
  console.log(`Generated ${path.relative(repoRoot, outFile)} from ${files.length} schemas.`);
}

main().catch((error) => {
  console.error(error.message);
  process.exit(1);
});

